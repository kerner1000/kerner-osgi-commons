package de.mpg.mpiz.koeln.anna.server.data;

public class DataBeanAccessException extends Exception {

	private static final long serialVersionUID = 4002480662305683330L;

	public DataBeanAccessException() {
		
	}

	public DataBeanAccessException(String arg0) {
		super(arg0);
		
	}

	public DataBeanAccessException(Throwable arg0) {
		super(arg0);
		
	}

	public DataBeanAccessException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

}
